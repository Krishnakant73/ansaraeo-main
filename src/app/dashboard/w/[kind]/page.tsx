// ============================================================
// /dashboard/w/[kind] — per-kind object picker.
//
// Lists every object of `kind` the user can access (RLS-filtered by
// the cookie client) and links to /dashboard/w/[kind]/[slug]. Kept
// intentionally deterministic — no descriptor.list() hook yet, just
// a switch by kind against known tables. Adding a new kind requires
// one case here.
// ============================================================

export const dynamic = "force-dynamic";

type Item = { id: string; label: string; sublabel?: string };

