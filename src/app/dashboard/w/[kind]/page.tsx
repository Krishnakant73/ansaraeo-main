async function loadItems(kind: string): Promise<Item[]> {
  const supabase = await createClient();
  if (kind === "brand") {
    const { data } = await supabase
      .from("brands")
      .select("id, name, slug, domain")
      .order("created_at", { ascending: false })
      .limit(50);
    return ((data as { id: string; name: string; slug: string; domain: string | null }[] | null) ?? []).map(
      (b) => ({ id: b.slug, label: b.name, sublabel: b.domain ?? undefined }),
    );
  }
  if (kind === "prompt") {
    const { data } = await supabase
      .from("prompts")
      .select("id, text, language")
      .order("priority", { ascending: false })
      .limit(50);
    return ((data as { id: string; text: string; language: string }[] | null) ?? []).map((p) => ({
      id: p.id,
      label: p.text.length > 90 ? p.text.slice(0, 90) + "…" : p.text,
      sublabel: p.language?.toUpperCase(),
    }));
  }
  if (kind === "competitor") {
    const { data } = await supabase
      .from("competitors")
      .select("id, name, domain, confirmed")
      .eq("confirmed", true)
      .order("created_at", { ascending: false })
      .limit(50);
    return ((data as { id: string; name: string; domain: string | null }[] | null) ?? []).map((c) => ({
      id: c.id,
      label: c.name,
      sublabel: c.domain ?? undefined,
    }));
  }
  if (kind === "campaign") {
    const { data } = await supabase
      .from("campaigns")
      .select("id, name, status, objective")
      .order("created_at", { ascending: false })
      .limit(50);
    return ((data as { id: string; name: string; status: string; objective: string | null }[] | null) ?? []).map(
      (c) => ({ id: c.id, label: c.name, sublabel: `${c.status} · ${c.objective ?? "no objective"}` }),
    );
  }
  if (kind === "mission") {
    const { data } = await supabase
      .from("missions")
      .select("id, title, status, priority")
      .order("priority", { ascending: false })
      .order("updated_at", { ascending: false })
      .limit(50);
    return ((data as { id: string; title: string; status: string; priority: number }[] | null) ?? []).map(
      (m) => ({ id: m.id, label: m.title, sublabel: `P${m.priority} · ${m.status.replace(/_/g, " ")}` }),
    );
  }
  if (kind === "sprint") {
    const { data } = await supabase
      .from("sprints")
      .select("id, name, status, goal, start_date, end_date")
      .order("start_date", { ascending: false, nullsFirst: false })
      .limit(50);
    return ((data as { id: string; name: string; status: string; goal: string | null; start_date: string | null; end_date: string | null }[] | null) ?? []).map(
      (s) => ({
        id: s.id,
        label: s.name,
        sublabel: `${s.status}${s.goal ? ` · ${s.goal}` : ""}`,
      }),
    );
  }
  if (kind === "engine") {
    const { data } = await supabase.from("engines").select("name, is_active").order("name");
    // Slug for engine is its name (chatgpt|perplexity|…), not a uuid.
    return ((data as { name: string; is_active: boolean }[] | null) ?? []).map((e) => ({
      id: e.name,
      label: e.name.replace(/_/g, " "),
      sublabel: e.is_active ? "active" : "disabled",
    }));
  }
  if (kind === "content") {
    const { data } = await supabase
      .from("content_items")
      .select("id, title, status, target_engine")
      .order("created_at", { ascending: false })
      .limit(50);
    return ((data as { id: string; title: string | null; status: string; target_engine: string | null }[] | null) ?? []).map(
      (c) => ({
        id: c.id,
        label: c.title || "Untitled draft",
        sublabel: `${c.status}${c.target_engine ? ` · ${c.target_engine.replace(/_/g, " ")}` : ""}`,
      }),
    );
  }
  if (kind === "opportunity") {
    const { data } = await supabase
      .from("opportunity_recommendations")
      .select("id, title, type, status, priority_score")
      .neq("status", "dismissed")
      .order("priority_score", { ascending: false })
      .limit(50);
    return ((data as { id: string; title: string; type: string; status: string; priority_score: number | null }[] | null) ?? []).map(
      (o) => ({
        id: o.id,
        label: o.title,
        sublabel: `${o.status} · ${o.type.replace(/_/g, " ")} · ${Math.round((o.priority_score ?? 0) * 100)}%`,
      }),
    );
  }
  if (kind === "automation") {
    const { data } = await supabase
      .from("automations")
      .select("id, name, is_active")
      .order("updated_at", { ascending: false })
      .limit(50);
    return ((data as { id: string; name: string; is_active: boolean }[] | null) ?? []).map((a) => ({
      id: a.id,
      label: a.name,
      sublabel: a.is_active ? "active" : "inactive",
    }));
  }
  if (kind === "alert") {
    const { data } = await supabase
      .from("geo_alert_rules")
      .select("id, metric, direction, window_type, is_active, threshold")
      .order("created_at", { ascending: false })
      .limit(50);
    return ((data as { id: string; metric: string; direction: string; window_type: string; is_active: boolean; threshold: number }[] | null) ?? []).map(
      (r) => ({
        id: r.id,
        label: r.metric.replace(/_/g, " "),
        sublabel: `${r.is_active ? "active" : "paused"} · ${r.direction} · ${r.window_type} · thr ${r.threshold}`,
      }),
    );
  }
  if (kind === "share") {
    const { data } = await supabase
      .from("share_view_tokens")
      .select("token, brand_id, expires_at, revoked, created_at")
      .order("created_at", { ascending: false })
      .limit(50);
    const rows = (data as { token: string; brand_id: string; expires_at: string; revoked: boolean; created_at: string }[] | null) ?? [];
    const brandIds = Array.from(new Set(rows.map((r) => r.brand_id)));
    const brandNames = new Map<string, string>();
    if (brandIds.length > 0) {
      const { data: bs } = await supabase
        .from("brands")
        .select("id, name")
        .in("id", brandIds);
      for (const b of (bs as { id: string; name: string }[] | null) ?? []) {
        brandNames.set(b.id, b.name);
      }
    }
    const now = Date.now();
    return rows.map((r) => {
      const brandName = brandNames.get(r.brand_id) ?? "brand";
      const expiresMs = new Date(r.expires_at).getTime();
      const state = r.revoked
        ? "revoked"
        : expiresMs < now
          ? "expired"
          : "live";
      return {
        id: r.token,
        label: `${brandName} · report link`,
        sublabel: state,
      };
    });
  }
  return [];
}

export default async function KindPicker({
  params,
}: {
  params: Promise<{ kind: string }>;
}) {
  const { kind } = await params;
  if (!has(kind)) notFound();
  const descriptor = getDescriptor(kind)!;
  // Descriptor-owned list() wins when defined — keeps picker query colocated
  // with the workspace. Fallback to the switch below for kinds not migrated.
  const items = descriptor.list ? await descriptor.list({ limit: 50 }) : await loadItems(kind);

  return (
    <div className="mx-auto max-w-3xl py-10">
      <p className="section-label">Workspace</p>
      <h1 className="mt-1 text-2xl font-bold tracking-tight text-ink capitalize">{kind}s</h1>
      <p className="mt-1 text-sm text-muted">
        Pick a {kind} to open its workspace — {descriptor.tabs.length} tabs each.
      </p>

      {items.length === 0 ? (
        <div className="mt-8">
          <EmptyStateCoach
            variant="coach"
            title={`No ${kind}s yet.`}
            description={hintForKind(kind)}
            action={actionForKind(kind)}
          />
        </div>
      ) : (
        <ul className="mt-6 space-y-2">
          {items.map((it) => (
            <li key={it.id}>
              <Link
                href={`/dashboard/w/${kind}/${it.id}/${descriptor.tabs[0]?.key ?? "overview"}`}
                className="group flex items-center justify-between rounded-2xl border border-line bg-white p-4 transition-colors hover:border-accent/40"
              >
                <div className="min-w-0">
                  <p className="truncate text-sm font-semibold text-ink group-hover:text-accent">
                    {it.label}
                  </p>
                  {it.sublabel && <p className="mt-0.5 truncate text-xs text-muted">{it.sublabel}</p>}
                </div>
                <ArrowRight className="h-4 w-4 text-muted transition-colors group-hover:text-accent" />
              </Link>
            </li>
          ))}
        </ul>
      )}
    </div>
  );
}
